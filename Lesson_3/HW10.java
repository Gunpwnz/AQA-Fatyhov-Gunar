package Lesson_3;

public class HW10 {
    public static void main(String[] args) {
        int[] numb = {1, 1, 0, 0, 1, 0, 1, 1, 0, 0};
        for (int i = 0; i < numb.length; i++) {
            if (numb[i] == 0) {
                System.out.println(numb[i] = 1);
            }
            else {
                System.out.println(numb[i] = 0);
            }
                
        }
    }

}
